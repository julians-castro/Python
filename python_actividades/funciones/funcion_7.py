def rango(numero, rango_inicial, rango_final):
    return rango_inicial <= numero <= rango_final

numero1 = 10
rango_inicial1 = 5
rango_final1 = 15

numero2 = 25
rango_inicial2 = 30
rango_final2 = 50

print(rango(numero1, rango_inicial1, rango_final1))  
print(rango(numero2, rango_inicial2, rango_final2))  
